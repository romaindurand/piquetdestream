		Les modalités de redistributions sont les suivantes:
	</p>
	<ul>
		<li>Être salarié(e), syndiqué(e) ou non</li>
		<li>
			Avoir cumulé un minimum de 2 jours de grève consécutifs (un(e) salarié(e) ayant, par exemple,
			fait plusieurs jours de grève non consécutifs dans le mois ne peut pas prétendre à une aide)
		</li>
		<li>
			S'être mis en grève contre un projet de loi, un projet d'accord national interprofessionnel.
			En l'occurrence, actuellement, contre le projet de loi sur les retraites
		</li>
	</ul>

	<p>
		Le gouvernement veut une nouvelle fois faire passer une réforme qu'une majorité de Françaises et
		de Français refusent. Une nouvelle fois nous nous y opposerons par tous les moyens à notre
		disposition.
	</p>

	<p>
