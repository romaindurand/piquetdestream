		Bonjour à toutes et à tous.<br />
		Dans le contexte actuel de grève générale, un projet de stream collaboratif se monte depuis quelques
		jours. Nous avons attendu de nous structurer un minimum avant de communiquer publiquement, bien que
		des sujets restent encore à éclaircir en interne, nous pouvons déjà nous présenter sur ces points
		:
	</p>
	<p>
		Nous sommes un collectif de streamers, de streameuses et de bénévoles qui nous mobilisons en
		soutien à la lutte contre la réforme des retraites.
	</p>
	<p>
		Nous lançons donc un événement en live sur une chaine Twitch créée pour l'occasion, avec
		plusieurs objectifs :
	</p>
	<ul>
		<li>Faire de l'éducation populaire sur la réforme des retraites, les mouvements sociaux,</li>
		<li>Inciter le plus grand nombre de personnes possible à participer à la grève</li>
		<li>Récolter des fonds pour soutenir les grévistes, syndiqué(e)s ou non</li>
		<li>
			Faire vivre la grève en ligne et montrer que nous sommes nombreuses et nombreux à la soutenir
		</li>
	</ul>

	<p>
		Pendant qu'iels tiendront le piquet de grève, nous tiendrons le <strong>Piquet De Stream</strong
		> !
	</p>

	<p>
		La journée du 19 janvier sera déterminante pour installer un rapport de force, et c'est dans le
		rapport de force que la victoire est possible. Par conséquent, le stream se lancera la veille,
		le mercredi 18 janvier, à 18h, pour anticiper les premiers conseils de manifs. Notre objectif
		n'est pas de proposer un contenu retransmis 24H/24. Il y aura une plage horaire de journée
		(10h-00h) qui sera remplie au maximum par toustes les streameureuses volontaires, et une plage
		nocturne qui sera là en complément mais de façon plus éparse (le lien du planning arrive sous
		peu).
	</p>

